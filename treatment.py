# The functions for the treatments of the types of the operations


def treatment(val1, val2):
    return val1 + val2


def treatmentmul(val1, val2):
    return val1 * val2


def treatmentdiv(val1, val2):
    return val1 / val2


def treatmentsub(val1, val2):
    return val1 - val2


def treatmentpower(val1, val2):
    return val1**val2
